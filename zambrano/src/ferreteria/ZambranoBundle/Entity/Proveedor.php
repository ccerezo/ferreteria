<?php

namespace ferreteria\ZambranoBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * proveedor
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="ferreteria\ZambranoBundle\Entity\proveedorRepository")
 */
class Proveedor
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="nombre", type="string", length=255)
     */
    private $nombre;

    /**
     * @var string
     *
     * @ORM\Column(name="telefono", type="string", length=255)
     */
    private $telefono;

    /**
     * @var string
     *
     * @ORM\Column(name="direccion", type="string", length=255)
     */
    private $direccion;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     * @return proveedor
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;
    
        return $this;
    }

    /**
     * Get nombre
     *
     * @return string 
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set telefono
     *
     * @param string $telefono
     * @return proveedor
     */
    public function setTelefono($telefono)
    {
        $this->telefono = $telefono;
    
        return $this;
    }

    /**
     * Get telefono
     *
     * @return string 
     */
    public function getTelefono()
    {
        return $this->telefono;
    }

    /**
     * Set direccion
     *
     * @param string $direccion
     * @return proveedor
     */
    public function setDireccion($direccion)
    {
        $this->direccion = $direccion;
    
        return $this;
    }

    /**
     * Get direccion
     *
     * @return string 
     */
    public function getDireccion()
    {
        return $this->direccion;
    }
    
    public function __toString(){
         return $this->nombre;
    }
    
    /**
     * @ORM\OneToMany(targetEntity="Articulo", mappedBy="proveedor")
     */
    private $articulo;
    public function __construct()
    {
        $this->articulo = new \Doctrine\Common\Collections\ArrayCollection();
    }
    public function addArticulo(\ferreteria\ZambranoBundle\Entity\Articulo $articulos)
    {
        $this->articulo[] = $articulos;
    }

    public function getArticulo()
    {
        return $this->articulo;
    }

}
